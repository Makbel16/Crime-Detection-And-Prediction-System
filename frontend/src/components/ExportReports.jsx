import React from 'react';
import { Download, FileText, FileSpreadsheet } from 'lucide-react';

const ExportReports = ({ crimes, stats }) => {
  // Export to CSV
  const exportToCSV = () => {
    if (!crimes || crimes.length === 0) {
      alert('No data to export');
      return;
    }

    const headers = ['ID', 'Crime Type', 'Latitude', 'Longitude', 'Date', 'Hour', 'Day', 'Month'];
    const csvContent = [
      headers.join(','),
      ...crimes.map(crime => [
        crime.id,
        crime.crime_type,
        crime.latitude,
        crime.longitude,
        crime.date,
        crime.hour,
        crime.day,
        crime.month
      ].join(','))
    ].join('\n');

    const blob = new Blob([csvContent], { type: 'text/csv;charset=utf-8;' });
    const link = document.createElement('a');
    const url = URL.createObjectURL(blob);
    link.setAttribute('href', url);
    link.setAttribute('download', `crime_data_${new Date().toISOString().split('T')[0]}.csv`);
    link.style.visibility = 'hidden';
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
  };

  // Export to Excel (as CSV with .xls extension for simplicity)
  const exportToExcel = () => {
    if (!crimes || crimes.length === 0) {
      alert('No data to export');
      return;
    }

    const headers = ['ID', 'Crime Type', 'Latitude', 'Longitude', 'Date', 'Hour', 'Day', 'Month', 'Hotspot Cluster'];
    const excelContent = [
      headers.join('\t'),
      ...crimes.map(crime => [
        crime.id,
        crime.crime_type,
        crime.latitude,
        crime.longitude,
        crime.date,
        crime.hour,
        crime.day,
        crime.month,
        crime.hotspot_cluster
      ].join('\t'))
    ].join('\n');

    const blob = new Blob([excelContent], { type: 'application/vnd.ms-excel;charset=utf-8;' });
    const link = document.createElement('a');
    const url = URL.createObjectURL(blob);
    link.setAttribute('href', url);
    link.setAttribute('download', `crime_report_${new Date().toISOString().split('T')[0]}.xls`);
    link.style.visibility = 'hidden';
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
  };

  // Generate Summary Report (Text)
  const generateReport = () => {
    if (!stats) {
      alert('No statistics available');
      return;
    }

    const report = `
CRIME ANALYSIS REPORT
Generated: ${new Date().toLocaleString()}
================================

SUMMARY STATISTICS
------------------
Total Crimes: ${stats.total_crimes}
Unique Crime Types: ${Object.keys(stats.crimes_by_type).length}

CRIMES BY TYPE
--------------
${Object.entries(stats.crimes_by_type)
  .sort((a, b) => b[1] - a[1])
  .map(([type, count]) => `${type}: ${count} (${((count / stats.total_crimes) * 100).toFixed(1)}%)`)
  .join('\n')}

CRIMES BY HOUR (Top 5)
----------------------
${Object.entries(stats.crimes_by_hour)
  .sort((a, b) => b[1] - a[1])
  .slice(0, 5)
  .map(([hour, count]) => `${hour}:00 - ${count} crimes`)
  .join('\n')}

CRIMES BY DAY OF WEEK
---------------------
${Object.entries(stats.crimes_by_day)
  .sort((a, b) => b[1] - a[1])
  .map(([day, count]) => `${getDayName(day)}: ${count} crimes`)
  .join('\n')}

RISK LEVELS
-----------
Low Risk: ${stats.risk_levels?.low || 0} areas
Medium Risk: ${stats.risk_levels?.medium || 0} areas
High Risk: ${stats.risk_levels?.high || 0} areas

================================
Report generated by Crime Hotspot Detection System
`.trim();

    const blob = new Blob([report], { type: 'text/plain;charset=utf-8;' });
    const link = document.createElement('a');
    const url = URL.createObjectURL(blob);
    link.setAttribute('href', url);
    link.setAttribute('download', `crime_report_${new Date().toISOString().split('T')[0]}.txt`);
    link.style.visibility = 'hidden';
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
  };

  const getDayName = (dayIndex) => {
    const days = ['Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday'];
    return days[dayIndex] || dayIndex;
  };

  return (
    <div className="bg-white rounded-lg shadow-md p-6 mb-6">
      <div className="flex items-center space-x-3 mb-4">
        <Download className="w-6 h-6 text-blue-600" />
        <h2 className="text-xl font-bold text-gray-900">Export Reports</h2>
      </div>
      
      <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
        {/* Export CSV */}
        <button
          onClick={exportToCSV}
          className="flex items-center justify-center space-x-2 px-4 py-3 bg-green-600 text-white rounded-lg hover:bg-green-700 transition-colors"
        >
          <FileSpreadsheet className="w-5 h-5" />
          <span>Export as CSV</span>
        </button>

        {/* Export Excel */}
        <button
          onClick={exportToExcel}
          className="flex items-center justify-center space-x-2 px-4 py-3 bg-blue-600 text-white rounded-lg hover:bg-blue-700 transition-colors"
        >
          <FileSpreadsheet className="w-5 h-5" />
          <span>Export as Excel</span>
        </button>

        {/* Generate Report */}
        <button
          onClick={generateReport}
          className="flex items-center justify-center space-x-2 px-4 py-3 bg-purple-600 text-white rounded-lg hover:bg-purple-700 transition-colors"
        >
          <FileText className="w-5 h-5" />
          <span>Summary Report</span>
        </button>
      </div>
    </div>
  );
};

export default ExportReports;
